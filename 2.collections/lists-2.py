countries = [
    'Argentina',
    'Brazil',
    'Chile',
    'Colombia',
    'Ecuador',
    'Peru',
    'Uruguay',
]
print(countries[1:-1]) # print range of items from list 1st index to last minus 1
print(countries[:3]) # print first 3 items
print(countries[3:]) # print from 3rd index to last
print(countries[::2]) # print with stepping of 2